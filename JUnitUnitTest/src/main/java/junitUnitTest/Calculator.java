package junitUnitTest;

public class Calculator {

	public int integerSubtraction(int minuend, int subtrahend) {
	    return minuend - subtrahend;
	}
	
	public int integerDivision(int dividened, int divsor) {
		return dividened/divsor;
		
	}
	
	public int integerAddition(int x, int y) {
		return x+y;
		
	}
	
	public String printString(String x, String y) {
		return x+" "+y;
	}
	
	public int integerMultiplication(int x, int y) {
		return x*y;
	}
}
